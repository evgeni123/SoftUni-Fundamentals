package java_fundamentals;

public class OpinionPollPerson {
    private String name;
    private int age;

    public OpinionPollPerson(String name, int age){
        this.name=name;
        this.age=age;
    }

    public String getName(){

        return this.name;
    }

    public int getAge(){

        return this.age;
    }
}
